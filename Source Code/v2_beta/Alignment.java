package wow.ishit.v2_beta_keimenoUI;

/**
 * Represents text alignment options for formatting content.
 * <p>
 * This enum defines four possible alignments that can be applied to text or UI components:
 * </p>
 * <ul>
 *   <li>{@link #LEFT}: Align text to the left edge.</li>
 *   <li>{@link #CENTER}: Center the text horizontally.</li>
 *   <li>{@link #RIGHT}: Align text to the right edge.</li>
 *   <li>{@link #NONE}: No alignment specified or default alignment.</li>
 * </ul>
 */
public enum Alignment {
    LEFT,
    CENTER,
    RIGHT,
    NONE
}
